package entity;
/**
 * 创建数据库字典实体类
 */

public class Dict {
    //存储用户级别
    String did;
    String dname;

    public String getDid() {
        return did;
    }

    public void setDid(String did) {
        this.did = did;
    }

    public String getDname() {
        return dname;
    }

    public void setDname(String dname) {
        this.dname = dname;
    }
}
